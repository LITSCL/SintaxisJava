package cl.litscl.sintaxisjava.bVariables;

public class Avariables {

	public static void main(String[] args) {
		
		//Declaración de Variables.
		int numero1;
		double numero2;
		float numero3;
		String texto1;
		boolean booleano1;
		
		//Iniciación de Variables.
		numero1 = 10;
		numero2 = 10.3;
		numero3 = 10.3F;
		texto1 = "Hola Mundo";
		booleano1 = true;
		
		//Declaración e iniciación de variables en una sola línea.
		int numero1v2 = 10;
		double numero2v2 = 10.3;
		float numero3v2 = 10.3F;
		String texto1v2 = "Hola Mundo";
		boolean booleano1v2 = true;
		
		//Declaración de múltiples variables de un mismo tipo en una sola línea.
		int numero1v3, numero2v3, numero3v3;
		
		//Declaración e iniciación de múltiples variables de un mismo tipo en una sola línea.
		int numero1v4 = 10, numero2v4 = 11, numero3v4 = 12;
		
	}
	
}
