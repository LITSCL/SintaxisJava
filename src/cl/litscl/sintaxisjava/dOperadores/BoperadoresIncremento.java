package cl.litscl.sintaxisjava.dOperadores;

public class BoperadoresIncremento {

	public static void main(String[] args) {
	
		int numero1 = 0;
		
		//Aumenta en una unidad el valor de una variable numerica.
		numero1++;
		++numero1;
		numero1+=1;
		
		System.out.println("La variable incrementada es igual a: " + numero1);

	}
	
}
