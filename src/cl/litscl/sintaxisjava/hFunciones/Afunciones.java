package cl.litscl.sintaxisjava.hFunciones;

public class Afunciones {
	
	//Una función permite agrupar un bloque de instrucciones, instrucciones las cuales se ejecutan al llamar la función desde su nombre.
	
	//Declarando la función.
	public static void miPrimeraFuncion() {
	    System.out.println("Instrucción 1");
	    System.out.println("Instrucción 2");
	    System.out.println("Instrucción 3");
	}
	
	public static void main(String[] args) {
		
		//Llamando la función.
		miPrimeraFuncion();
		
	}

}
