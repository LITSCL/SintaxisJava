package cl.litscl.sintaxisjava.eConversionDeTiposDeDatos;

public class BintAfloat {
	
	public static void main(String[] args) {
		
		int numeroInt = 10; //Se declara e inicia la variable de tipo int.
		
		float numeroFloat = (float)numeroInt; //Se refunde el dato de tipo int a float.
		
		System.out.println(numeroFloat);
		
	}
	
}
